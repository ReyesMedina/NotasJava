package Operadores;

public class OperadoresUnarios {
    public static void main(String[] args) {
        //Son muy usados para invertir signos
        int i = -5;
        System.out.println("i = " + i);
        int j = +i; // j= (1)*-5 => -5
        System.out.println("j = " + j);
        int k = -i; // j= (-1)*-5 => 5
        System.out.println("k = " + k);

    }
}
